################ Python - Copy


######## 1-Copy a List

thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)

######## 2-Use the list() method

thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)
print(mylist)


######## 3-Use the slice Operator


thislist = ["apple", "banana", "cherry"]
mylist = thislist[:]
print(mylist)